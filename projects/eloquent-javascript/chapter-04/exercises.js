// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
 let finArr = [];
 //if there is a step argument
 if(step){
   if(step > 0){
     for(let i = start; i <=end; i += step){
        finArr.push(i);
       }
    }  
 } 
//if no step arg
else {
//if start is not equal to end
   if(start !== end){
//standard loop push i to array
     for(let i = start; i <=end; i++){
       finArr.push(i);
     }
   }
 }
 return finArr;
}

